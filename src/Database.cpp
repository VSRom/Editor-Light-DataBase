#include "Database.h"
//===========================================================================================================
Database::Database()
{ }
//===========================================================================================================
Database::~Database()
{ }
//===========================================================================================================
bool Database::isOpen() const
{
    return db_Q.isOpen();
}
//===========================================================================================================
bool Database::init_db(const QString &connect_name)
{
    db_Q = QSqlDatabase::database(connect_name);

    if (!db_Q.open())
        return false;

    else
        return true;
}
